    private void performPasswordReset() {
        String email = emailText.getText().trim();
        String newPassword = new String(passwordText.getPassword());
        String confirmPassword = new String(confirmPasswordText.getPassword());

        if (email.isEmpty() || newPassword.isEmpty() || confirmPassword.isEmpty() || email.equals("Email...")) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Email validation regex
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        if (!email.matches(emailRegex)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!newPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "New passwords do not match.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (newPassword.length() < 6) {
            JOptionPane.showMessageDialog(this, "Password must be at least 6 characters long.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        LoadingDialog.showLoading(this, "Resetting password...");
        SwingWorker<Void, Void> worker = new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                Firestore db = FirebaseService.getFirestore();
                
                // Find user by email
                QuerySnapshot query = db.collection("users").whereEqualTo("email", email).get().get();
                
                if (query.isEmpty()) {
                    throw new Exception("No user found with that email.");
                }
                
                // It should be unique, so just take the first one
                QueryDocumentSnapshot userDoc = query.getDocuments().get(0);
                
                String salt = BCrypt.gensalt();
                String hashedPassword = BCrypt.hashpw(newPassword, salt);
                
                // Update password
                userDoc.getReference().update("password_hash", hashedPassword).get();
                
                return null;
            }

            @Override
            protected void done() {
                try {
                    get();
                    JOptionPane.showMessageDialog(ForgotPassword.this, "Password reset successfully! Please log in.", "Success", JOptionPane.INFORMATION_MESSAGE);
                    navigateToLogin();
                } catch (ExecutionException ex) {
                     if (ex.getCause() != null && ex.getCause() instanceof Exception) {
                         JOptionPane.showMessageDialog(ForgotPassword.this, ex.getCause().getMessage(), "Reset Failed", JOptionPane.ERROR_MESSAGE);
                     } else {
                         JOptionPane.showMessageDialog(ForgotPassword.this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                         logger.log(java.util.logging.Level.SEVERE, "Reset error", ex);
                     }
                } catch (InterruptedException ex) {
                    logger.log(java.util.logging.Level.SEVERE, "Reset interrupted", ex);
                } finally {
                    LoadingDialog.hideLoading();
                }
            }
        };
        worker.execute();
    }
    
